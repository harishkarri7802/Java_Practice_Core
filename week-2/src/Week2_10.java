//Program to find the maximum value of different primitive data types using wrapper class constants.
public class Week2_10 {
    public static void main(String[] args) {

        System.out.println("Integer Max value : " + Integer.MAX_VALUE);
        System.out.println("Long Max value : " + Long.MAX_VALUE);
        System.out.println("Flout Max value : " + Float.MAX_VALUE);
        System.out.println("Double Max value : " + Double.MAX_VALUE);
        System.out.println("Character Max value : " + Character.MAX_VALUE);
        System.out.println("Byte Max value : " + Byte.MAX_VALUE);
        System.out.println("Short Max value : " + Short.MAX_VALUE);

    }
}
